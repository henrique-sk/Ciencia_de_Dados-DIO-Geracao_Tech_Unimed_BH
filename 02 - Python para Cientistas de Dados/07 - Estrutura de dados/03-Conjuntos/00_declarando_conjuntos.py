# o SET elimina valores duplicados em uma lista
numeros = set([1, 2, 3, 1, 3, 4])  # {1, 2, 3, 4}
print(numeros)

letras = set("abacaxi")  # {"b", "a", "c", "x", "i"}
print(letras)

carros = set(("palio", "gol", "celta", "palio"))  # {"gol", "celta", "palio"}
print(carros)

linguagens = {"python", "java", "python"}
print(linguagens)